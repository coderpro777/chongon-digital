<b>Pausa o cancela una descarga</b><br><br>
1. En la parte inferior, encuentre el archivo de descarga que desea pausar o cancelar.<br><br>
2. Junto al nombre del archivo en la parte inferior de la pantalla, haga clic en la flecha hacia abajo o Más.<br><br>
3. Haga clic en Pausa, Reanudar o Cancelar.<br><br>
<b>Ver una lista de archivos que has descargado</b><br><br>
1. En su computadora, abra Chrome.<br><br>
2. En la parte superior derecha, haga clic en Más descargas.<br><br>
• Para abrir un archivo, haga clic en su nombre. Se abrirá en la aplicación predeterminada de su computadora para el tipo de archivo.<br><br>
• Para eliminar una descarga de su historial, a la derecha del archivo, haga clic en Eliminar. El archivo se eliminará de su página de descargas en Chrome, no de su computadora.<br><br>
