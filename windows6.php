1. En su computadora, abra Chrome.<br><br>
2. En la parte superior derecha, haga clic en Más configuraciones.<br><br>
3. En la parte inferior, haga clic en Avanzado.<br><br>
4. En la sección "Descargas", ajusta la configuración de descarga:<br><br>
• Para cambiar la ubicación de descarga predeterminada, haga clic en Cambiar y seleccione dónde desea guardar sus archivos.<br><br>
• Si prefiere elegir una ubicación específica para cada descarga, marque la casilla junto a "Preguntar dónde guardar cada archivo antes de descargar".<br><br>
