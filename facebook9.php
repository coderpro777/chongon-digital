            <li><h3 style="color:#36aad9;">8. Cómo crear un grupo en Facebook</h3><p ALIGN="justify">
Los grupos de Facebook se suelen utilizar como un espacio de debate o de intercambio de información, ideas o productos entre distintos usuarios de Facebook que tengan interés en un mismo tema.<br>
Según Facebook, “los grupos son la opción ideal para concretar muchos temas y estar en contacto con las personas que quieres. Comparte fotos y vídeos, mantén conversaciones, haz planes y más”.<br>
Por ejemplo, existen grupos para españoles residentes en Londres, en los que se comparten trucos, curiosidades, consejos o eventos que sean relevantes para todo español que viva en la capital británica. ¿Sobre qué crearías tú un grupo?
Si tienes clara la idea, y quieres sí o sí crear un espacio de intercambio de ideas, vuelva a la página de ‘Inicio’, en el margen izquierdo haz clic sobre ‘Grupos’ y después haz clic sobre ‘Crear grupo’.<br>
Se abrirá una ventana en la que tendrás que ingresar el nombre que has elegido para tu grupo, y también tendrás que agregar a algunas personas al grupo.
Puedes elegir el tipo de privacidad que deseas, existen tres opciones al respecto, que Facebook estructura en:<br><br>
• Grupo público: cualquiera puede ver el grupo, los miembros y sus publicaciones.<br>

• Grupo cerrado: cualquiera puede buscar el grupo y ver quién pertenece a él. Solo los miembros pueden ver las publicaciones.<br>

• Grupo secreto: solo los miembros pueden buscar en el grupo y ver las publicaciones.  



