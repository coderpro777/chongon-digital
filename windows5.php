            <b>Crear una nueva carpeta</b><br><br>
            En la columna derecha del Explorador de Windows, haga clic derecho en el espacio en blanco, asegurándose de que no haya nada resaltado. Mueva el cursor sobre la palabra Nuevo, haga clic en Carpeta y escriba el nombre de la carpeta. Si pierde la oportunidad de escribir el nombre, a su carpeta se le dará el nombre de Nueva carpeta.<br><br>
            <b>Renombrar una carpeta</b><br><br>
            Para cambiar el nombre de una carpeta, haga clic derecho en el objeto y haga clic en cambiar nombre. O haga clic en el objeto una vez, haga una pausa, vuelva a hacer clic y escriba el nuevo nombre. Al cambiar el nombre de los archivos, asegúrese de mantener el período y la extensión del archivo intactos.<br><br><b>Copiar una carpeta o archivo</b><br><br>
            Para copiar una carpeta o archivo, haga clic derecho en el objeto y haga clic en Copiar. Luego, en el espacio en blanco de la columna derecha - asegurándose de que no haya nada resaltado - haga clic derecho y haga clic en Pegar. La carpeta o el archivo copiado se colocará en la parte inferior de la lista.<br><br>

            Para mover un archivo a otra carpeta, use uno de estos 2 métodos:<br><br>
            Método 1: Haga clic derecho en el archivo y haga clic en Cortar. Encuentra la carpeta receptora o de destino en la columna izquierda. Haga clic derecho en la carpeta y haga clic en Pegar. (O, para hacerlo seguro, copie y pegue, y luego regrese y elimine el archivo de la ubicación original una vez que haya verificado que se encuentra en la nueva ubicación).<br><br>
            Método 2 - "Arrastrar y soltar": haga clic en la carpeta correspondiente en la columna izquierda para que su archivo se muestre en la columna derecha. Luego ubique, pero no haga clic en, la carpeta de destino en la columna izquierda. (Haga clic en + delante de una carpeta para mostrar las carpetas que contiene.) Haga clic y arrastre el archivo en la columna derecha hacia la carpeta de destino en la columna izquierda y, cuando la carpeta de destino se resalte, suelte el botón del mouse. El archivo caerá en la carpeta.<br><br>
            Asegúrese de que la carpeta correcta esté resaltada antes de soltar el botón del mouse o su archivo puede caer en la carpeta incorrecta. Esto requiere un poco de práctica, pero es una función ingeniosa.

